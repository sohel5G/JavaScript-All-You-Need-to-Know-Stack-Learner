// &&  and operator 
// || or operator 
// ! not operator
// !! 

var a = 10
var b = 20
var c = 30 
var d = 40

if ( a > b && c > d ) {
    console.log("True")
}else{
    console.log("False")
}


if ( a > b || c < d ) {
    console.log(' True ')
}


if ( !(a > b) ) {
    console.log('True')
}


if ( !!(a > b) ) {
    console.log('false')
}else{
    console.log('True')
}


